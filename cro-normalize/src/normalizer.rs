/// Vraća mapiranje dijakritika iz statičke mape (uključujući dijalekte).
pub fn get_diacritic_mapping(word: &str) -> Option<&'static str> {
    DIACRITIC_MAP.get(word).copied().or_else(|| DIALECT_MAP.get(word).copied())
}

